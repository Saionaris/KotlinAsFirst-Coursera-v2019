 */
fun squareSequenceDigit(n: Int): Int {
    var a = 0
    var b = 0
    var c = 0
    while (a < n) {
        b += 1
        c = sqr(b)
        a += digitNumber(c)
    }
    for (i in 1..(a - n)) c /= 10
    return c % 10
}

/**
 * Сложная
 *
 * Найти n-ю цифру последовательности из чисел Фибоначчи (см. функцию fib выше):
 * 1123581321345589144...
 * Например, 2-я цифра равна 1, 9-я 2, 14-я 5.
 *
 * Использовать операции со строками в этой задаче запрещается.
 */
fun fibSequenceDigit(n: Int): Int {
    var fib1 = 1
    var fib2 = 1
    while (fib1 < n) {
        ++fib2
        var fibRes = fib(fib2)
        while (fibRes > 0) {
            fibRes /= 10
            ++fib1
        }
    }
    var fibRes = fib(fib2)
    for (i in 1..fib1 - n) {
        fibRes /= 10
    }
    return fibRes % 10
}
